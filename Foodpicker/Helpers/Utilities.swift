import Foundation

class Utilities {
    static func isPasswordValid(_ password : String) -> Bool {
        let testPassword = NSPredicate(format: "SELF MATCHES %@", "^(?=.*[a-z])(?=.*[$@$#!%*?&])[A-Za-z\\d$@$#!%*?&]{8,}")
        
        return testPassword.evaluate(with: password)
    }
}
